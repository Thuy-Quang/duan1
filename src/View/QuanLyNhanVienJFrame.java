    private void showData(List<NhanVien> list) {
        int stt = 1;
        dtm.setRowCount(0);

        for (NhanVien nhanVien : list) {
            Object data[] = {
                stt++,
                nhanVien.getID(),
                nhanVien.getUserName(),
                nhanVien.getHoTen(),
                getChucVu(nhanVien.getChucVu()),
                nhanVien.getSoDienThoai()
            };
            dtm.addRow(data);
        }
    }

    private void detailData(NhanVien nhanVien) {
        txtID.setText(String.valueOf(nhanVien.getID()));
        txtTenDangNhap.setText(nhanVien.getUserName());
        txtHoTen.setText(nhanVien.getHoTen());
        txtMatKhau.setText(nhanVien.getMatKhau());
        if (nhanVien.getChucVu() == 1) {
            rdoNhanVien.setSelected(true);
        } else {
            rdoQuanLy.setSelected(true);
        }
        txtSDt.setText(nhanVien.getSoDienThoai());

    }

    private String getChucVu(int chucVu) {
        switch (chucVu) {
            case 0:
                return "Quản lý ";
            case 1:
                return "Nhân viên";
            default:
                return "";

        }
    }

    boolean checkForm() {

        if (txtTenDangNhap.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên đang nhập không được để trống");
            txtTenDangNhap.requestFocus();
            return false;
        }
        
        if (txtMatKhau.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mật khẩu không được để trống !");
            txtMatKhau.requestFocus();
            return false;
        }

        if (txtHoTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Họ và tên không được để trống");
            txtHoTen.requestFocus();
            return false;
        }
        if (txtSDt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được để trống");
            txtSDt.requestFocus();
            return false;
        }
        try {

        } catch (Exception e) {
        }
        Pattern p = Pattern.compile("^[0-9]{10}$");
        if (!p.matcher(txtSDt.getText()).find()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại phải là số và có 10 số");
            txtSDt.requestFocus();
            return false;
        }
        String soDienThoai = txtSDt.getText();
        
         if (soDienThoai.charAt(0) != '0') {
                JOptionPane.showMessageDialog(this, "Số đầu tiền trong số điện thoại phải là '0'");
                return false;
         }
//        Pattern ps = Pattern.compile("^[0]{10}$");
//        if (ps.matcher(txtSDt.getText()).find()) {
//            JOptionPane.showMessageDialog(this, "Số điện thoại phải bắt đầu bằng số 0");
//            txtSDt.requestFocus();
//            return false;
//        }

        return true;
    }
    
