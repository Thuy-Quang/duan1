    public void detailDataD(int index) {
        
        cboTrangThai.setSelectedIndex(list.get(index).getTrangthai());
        

    }
    
    public void firstNV() {

        index = 0;

        tblQuanLyHoaDon.setRowSelectionInterval(index, index);
        detailDataD(index);
        
    }

    public void lastNV() {
        index = list.size() - 1;

        tblQuanLyHoaDon.setRowSelectionInterval(index, index);
        detailDataD(index);
    

    }

    public void PreNV() {
        if (index == 0) {
            lastNV();
        } else {
            index--;
            tblQuanLyHoaDon.setRowSelectionInterval(index, index);
            detailDataD(index);
        
        }

    }

    public void NextNV() {
        if (index == list.size()) {
            firstNV();
        } else {
            index++;
            tblQuanLyHoaDon.setRowSelectionInterval(index, index);
            detailDataD(index);
      
        }

    }



