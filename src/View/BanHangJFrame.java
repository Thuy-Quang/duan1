    private boolean checkVoucher() throws SQLException {
        List<Voucher> listVoucher = voucherDAO.getAll();
        
        for (Voucher voucher : listVoucher) {
            if (Integer.parseInt(txtMaGiamGia.getText()) == voucher.getId()) {
                return true;
            }
        }
        return false;
    }

