    private void btnResetPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetPasswordActionPerformed
        // TODO add your handling code here:
        if (!txtNewPassword.getText().isEmpty() && !txtConfirmNewPassword.getText().isEmpty()) {
                String username = txtUsername.getText();
                String newPassword = String.valueOf(txtNewPassword.getPassword());
                String confirmNewPassword = String.valueOf(txtConfirmNewPassword.getPassword());

                if (newPassword.equals(confirmNewPassword)) {
                    try {
                        JOptionPane.showMessageDialog(this, nhanVienDAO.forgotPassword(username, newPassword));
                    } catch (SQLException ex) {
                        ex.printStackTrace();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "2 trường mật khẩu không giống nhau \n Vui lòng nhập lại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập giá trị cho mật khẩu và xác nhận mật khẩu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnResetPasswordActionPerformed

    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        boolean check = false;

        for (NhanVien nhanVien : list) {
            if (username.equals(nhanVien.getUserName())) {
                check = true;
                break;
            }
        }

        if (check) {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập đúng, hãy nhập đúng mật khẩu và xác nhận \nmật khẩu rồi bấm đổi mật khẩu để đổi mật khẩu mới!");
            txtNewPassword.setEnabled(true);
            txtConfirmNewPassword.setEnabled(true);
            cboHidePassword.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập vừa nhập không tồn tại trong cơ sở dữ liệu!");
        }
    }//GEN-LAST:event_btnCheckActionPerformed

    private void cboHidePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboHidePasswordActionPerformed
        // TODO add your handling code here:
        if(cboHidePassword.isSelected()) {
            txtNewPassword.setEchoChar('\u0000');
            txtConfirmNewPassword.setEchoChar('\u0000');
        } else {
            txtNewPassword.setEchoChar('*');
            txtConfirmNewPassword.setEchoChar('*');
        }
    }//GEN-LAST:event_cboHidePasswordActionPerformed

