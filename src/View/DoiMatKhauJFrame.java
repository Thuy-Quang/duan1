    private void cboHidePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboHidePasswordActionPerformed
        // TODO add your handling code here:
        if(cboHidePassword.isSelected()) {
            txtNewPassword.setEchoChar('\u0000');
            txtCurrentPassword.setEchoChar('\u0000');
            txtConfirmNewPassword.setEchoChar('\u0000');
        } else {
            txtNewPassword.setEchoChar('*');
            txtCurrentPassword.setEchoChar('*');
            txtConfirmNewPassword.setEchoChar('*');
        }
    }//GEN-LAST:event_cboHidePasswordActionPerformed

    private void btnResetPassword1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetPassword1ActionPerformed
        // TODO add your handling code here:
        if (!txtCurrentPassword.getText().isEmpty() && !txtNewPassword.getText().isEmpty() && !txtConfirmNewPassword.getText().isEmpty()) {
            String currentPassword = String.valueOf(txtCurrentPassword.getPassword());
            String newPassword = String.valueOf(txtNewPassword.getPassword());
            String confirmNewPassword = String.valueOf(txtConfirmNewPassword.getPassword());

            if (newPassword.equals(confirmNewPassword)) {
                try {
                    JOptionPane.showMessageDialog(this, nhanVienDAO.changePassword(loggedInUser, currentPassword, newPassword));
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(this, "2 trường mật khẩu mới và xác nhận không giống nhau \n Vui lòng nhập lại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập giá trị cho mật khẩu và xác nhận mật khẩu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnResetPassword1ActionPerformed

