    private void showData(List<KhachHang> list) {
        int stt = 1;
        dtm.setRowCount(0);

        for (KhachHang khachHang : list) {
            Object data[] = {
                stt++,
                khachHang.getId(),
                khachHang.getHoVaTen(),
                khachHang.getSoDienThoai(),
                khachHang.getDiaChi(),};
            dtm.addRow(data);
        }
    }

    private void detailData(KhachHang khachHang) {
        txtID.setText(String.valueOf(khachHang.getId()));
        txtTenKH.setText(khachHang.getHoVaTen());
        txtSDT.setText(khachHang.getSoDienThoai());
        txtDiaChi.setText(khachHang.getDiaChi());
    }

    private void customShowData(DefaultTableModel model, List<KhachHang> list) {
        int stt = 1;
        model.setRowCount(0);
        for (KhachHang khachHang : list) {
            Object obj[] = {
                stt++,
                khachHang.getId(),
                khachHang.getHoVaTen(),
                khachHang.getSoDienThoai(),
                khachHang.getDiaChi(),};
            model.addRow(obj);
        }
    }

    boolean checkForm() {
        if (txtTenKH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên khách hàng không được để trống");
            txtTenKH.requestFocus();
            return false;
        }

        String soDienThoai = txtSDT.getText();
        if (soDienThoai.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được để trống");
            txtSDT.requestFocus();
            return false;
        } else {
            try {
                int sdt = Integer.parseInt(soDienThoai);
                Pattern p = Pattern.compile("^[0-9]{10}$");
                if (!p.matcher(soDienThoai).find()) {
                    JOptionPane.showMessageDialog(this, "Số điện thoại phải là số và có 10 số");
                    txtSDT.requestFocus();
                    return false;
                }
                if (soDienThoai.charAt(0) != '0') {
                    JOptionPane.showMessageDialog(this, "Số đầu tiên trong số điện thoại phải là '0'");
                    return false;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Số điện thoại phải là số!");
                return false;
            }
        }

        return true;
    }
